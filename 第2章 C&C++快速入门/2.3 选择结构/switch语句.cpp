#include<iostream>
using namespace std;
int main(){
    int n;
    cin>>n;
    if(n==0)
        cout<<"Zero";
    if(n==1)
        cout<<"One";
    if(n==2)
        cout<<"Two";
    if(n==3)
        cout<<"Three";
    if(n==4)
        cout<<"Four";
    if(n==5)
        cout<<"Five";
    if(n>5)
        cout<<"Greater than 5";
    return 0;
}
